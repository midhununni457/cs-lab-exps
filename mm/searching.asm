DATA SEGMENT
    ARR DW 1234H, 3ACDH, 0F3CH, 76ABH, 9D0CH
    N EQU 5
    KEY DW 76ABH

    FOUND_MSG DB 10,13 'Found!$'
    NOT_FOUND_MSG DB 10,13 'Not Found!$'
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE, DS:DATA

START:
    MOV AX, DATA
    MOV DS, AX

    LEA SI, ARR
    MOV CX, N

SEARCH_LOOP:
    MOV AX, [SI]
    CMP AX, KEY
    JE FOUND

    ADD SI, 2
    JMP SEARCH_LOOP

    LEA AX, NOT_FOUND_MSG
    MOV AH, 09H
    INT 21H
    JMP EXIT

FOUND:
    LEA AX, FOUND_MSG
    MOV AH, 09H
    INT 21H

EXIT:
    MOV AH, 4CH
    INT 21H

CODE ENDS
END START