DATA SEGMENT
    NUM1_LOW DW 1234H
    NUM1_HIGH DW 5678H
    NUM2_LOW DW 9ABCH
    NUM2_HIGH DW 0FEDH
    
    RES_LOW DW ?
    RES_HIGH DW ?

    MSG DB 10,13 'SUM: $'
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE, DS:DATA

START:
    MOV AX, DATA
    MOV DS, AX

    MOV AX, NUM1_LOW
    ADD AX, NUM2_LOW
    MOV RES_LOW, AX

    MOV AX, NUM1_HIGH
    ADC AX, NUM2_HIGH
    MOV RES_HIGH, AX

    LEA DX, MSG
    MOV AH, 09H
    INT 21H

    MOV BX, RES_HIGH
    CALL PRINT_HEX

    MOV BX, RES_LOW
    CALL PRINT_HEX

    MOV AH, 4CH
    INT 21H

PRINT_HEX PROC
    MOV CX, 4
NEXT:
    ROL BX, 4
    MOV AL, BL
    AND AL, 0FH
    ADD AL, 30H
    CMP AL, '9'
    JBE DISP
    ADD AL, 7
DISP:
    MOV DL, AL
    MOV AH, 02H
    INT 21H
    LOOP NEXT
    RET
PRINT_HEX ENDP

CODE ENDS
END START