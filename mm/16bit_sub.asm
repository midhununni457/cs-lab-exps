DATA SEGMENT
    NUM1 DW 4321H
    NUM2 DW 1234H
    DIFF DW ?
    MSG DB 10,13 'DIFF (HEX): $'
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE, DS:DATA

START:
    MOV AX, DATA
    MOV DS, AX

    MOV AX, NUM1
    SUB AX, NUM2
    MOV DIFF, AX

    LEA DX, MSG
    MOV AH, 09H
    INT 21H

    MOV BX, DIFF
    CALL PRINT_HEX

    MOV AH, 4CH
    INT 21H

PRINT_HEX PROC
    MOV CX, 4
NEXT:
    ROL BX, 4
    MOV AL, BL
    AND AL, 0FH
    ADD AL, 30H
    CMP AL, '9'
    JBE DISP
    ADD AL, 7
DISP:
    MOV DL, AL
    MOV AH, 02H
    INT 21H
    LOOP NEXT
    RET
PRINT_HEX ENDP

CODE ENDS
END START